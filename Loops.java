public class Loops {
    public static void main(String[] args) {
        // for , while , do while
        for (int i = 1; i <= 100; i++) {

        }
        int j = 1;
        while (j <= 10) {
            j++;
        }
        int k = 1;
        do {
            k++;
        } while (k <= 10);
    }
}
