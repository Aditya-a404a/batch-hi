class Second {
    // main - it is function, entry point of a program
    // void - keyword - nothing to return
    // static - keyword - things will be loaded when class is loaded
    // public - keyword - access the things outside the folder/package
    // String args[] - this is called command line argument
    // String - predefine class, collection of chars
    // args[] - args name of argument [] - array

    public static void main(String args[]) {
        // out - object , PrintStream class
        // println- print with new line
        // print - print no new line
        // System - predefine class - out , in ,err object already created.
        System.out.println("Hello Java");
    }
}